    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        String cp = jTextFieldCpf.getText();
        String no = jTextFieldNome.getText();
        String ch = jTextFieldChapa.getText();
        //String set = jTextFieldSetor.getText();
        
        
        Funcionario funcionario = new Funcionario();
        funcionario.setCpf(cp);
        funcionario.setNome(no);
        funcionario.setChapa(ch);
        //cliente.setRg(r);
       // funcionario.setSetor(set);
        
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        int resultado = funcionarioDAO.insere(funcionario);
        if(resultado == 1){
            JOptionPane.showMessageDialog(null, "Registro inserido com sucesso!");
        }
        else{
            JOptionPane.showMessageDialog(null, "Problema na inserção");
        }
        
        jTextFieldNome.setText("");
        jTextFieldCpf.setText("");
        jTextFieldChapa.setText("");
        //jTextFieldSetor.setText("");
 
    
    }//GEN-LAST:event_jButtonInserirActionPerformed


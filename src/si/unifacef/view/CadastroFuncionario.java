    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        // TODO add your handling code here:
        dtm.setNumRows(0);
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
            ArrayList<Funcionario> funcionarios = funcionarioDAO.consulta();
            for(int i=0; i < funcionarios.size(); i++){
                String[] objeto = new String[6];
                objeto[0] = String.valueOf(funcionarios.get(i).getCodigof());
                objeto[1] = (funcionarios.get(i).getNome());
                objeto[2] = (funcionarios.get(i).getCpf());
                objeto[3] = (funcionarios.get(i).getChapa());
                objeto[4] = (funcionarios.get(i).getSetor());
                dtm.addRow(objeto);
            }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

